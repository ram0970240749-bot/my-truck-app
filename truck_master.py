import streamlit as st
import pandas as pd

# ตั้งค่าหน้าตาแอปและธีมให้กว้างเต็มจอ
st.set_page_config(page_title="ระบบจัดการรถขนส่ง TruckMaster", page_icon="🚚", layout="wide")

# ==========================================
# 💾 ฐานข้อมูลหลัก (หัวข้อภาษาไทยทั้งหมดตามต้องการ)
# ==========================================
if 'trucks_db' not in st.session_state:
    st.session_state['trucks_db'] = pd.DataFrame([
        {"ทะเบียนรถ": "TRK-01", "ชื่อคนขับ": "สมชาย ใจดี", "สิ่งที่บรรทุก": "ข้าวสาร 20 ตัน", "เวลาคาดว่าจะถึง (ETA)": "14:30", "สถานะ": "กำลังวิ่ง", "ค่าน้ำมัน (บาท)": 2500, "กำไร (บาท)": 4500, "latitude": 13.7563, "longitude": 100.5018},
        {"ทะเบียนรถ": "TRK-02", "ชื่อคนขับ": "วิชัย รักดี", "สิ่งที่บรรทุก": "เหล็กเส้น 15 ตัน", "เวลาคาดว่าจะถึง (ETA)": "18:00", "สถานะ": "จอดพัก", "ค่าน้ำมัน (บาท)": 3000, "กำไร (บาท)": 6200, "latitude": 14.9738, "longitude": 102.0836},
        {"ทะเบียนรถ": "TRK-03", "ชื่อคนขับ": "อนันต์ ยอดเยี่ยม", "สิ่งที่บรรทุก": "อาหารแช่แข็ง", "เวลาคาดว่าจะถึง (ETA)": "ถึงแล้ว", "สถานะ": "ส่งงานเสร็จแล้ว", "ค่าน้ำมัน (บาท)": 2200, "กำไร (บาท)": 3800, "latitude": 12.9236, "longitude": 100.8824}
    ])

df = st.session_state['trucks_db']

# ==========================================
# ⚙️ ส่วนที่ 1: เมนูแถบด้านข้าง (Sidebar) เหมือนในรูปภาพเป๊ะ
# ==========================================
with st.sidebar:
    st.header("⚙️ ศูนย์ตั้งค่าระบบ")
    st.write("ตั้งค่าผู้ใช้งานในระบบ:")
    
    # เมนูเลือกสิทธิ์ใช้งานเลียนแบบกล่องเลือกในรูปภาพ
    user_role = st.selectbox(
        "สิทธิ์ผู้ใช้งานปัจจุบัน:",
        ["เจ้าของธุรกิจ (Owner)", "พนักงานบัญชี (Accountant)", "พนักงานขับรถ (Driver)"]
    )
    
    st.markdown("---")
    st.subheader("🛠️ การบำรุงรักษา & แจ้งเตือน")
    st.error("⚠️ TRK-01: ถึงระยะเลื่อนกำหนดซ่อมบำรุงแล้ว")
    st.info("ℹ️ TRK-02: ค่าน้ำมันเฉลี่ยคงที่ในสัปดาห์นี้")

# ==========================================
# 👑 ส่วนที่ 2: หน้าจอแสดงผลหลักตามสิทธิ์การใช้งาน
# ==========================================

# แสดงหัวข้อหลักของแอป
st.title("🚚 ระบบบริหารจัดการขนส่งเรียลไทม์")

# 🟢 กรณีที่ 1: สิทธิ์เจ้าของธุรกิจ (ทำได้ทุกฟังก์ชัน แก้ตารางได้ทุกช่อง หัวข้อภาษาไทย)
if user_role == "เจ้าของธุรกิจ (Owner)":
    st.subheader("📊 แดชบอร์ดผู้บริหาร (สิทธิ์: เจ้าของธุรกิจ)")
    
    tab1, tab2 = st.tabs(["📋 สรุปข้อมูลตารางรถทั้งหมด", "🗺️ แผนที่พิกัดเรียลไทม์"])
    
    with tab1:
        st.write("💡 เถ้าแก่สามารถดับเบิ้ลคลิกแก้ไขตัวเลขหรือข้อความในตารางภาษาไทยนี้ได้ทันที:")
        # เปิดตารางให้แก้ไขได้สดๆ เพิ่มแถวรถใหม่ได้อัตโนมัติ
        edited_df = st.data_editor(df, use_container_width=True, num_rows="dynamic", hide_index=True)
        
        if st.button("💾 บันทึกข้อมูลที่แก้ไขทั้งหมด", use_container_width=True, type="primary"):
            st.session_state['trucks_db'] = edited_df
            st.success("✅ บันทึกการเปลี่ยนแปลงในตารางลงระบบเรียบร้อยแล้ว!")
            st.rerun()
            
    with tab2:
        st.map(df)

# 💵 กรณีที่ 2: สิทธิ์พนักงานบัญชี (แก้ไขเรื่องเงินค่าน้ำมันและกำไรได้ในตารางภาษาไทย)
elif user_role == "พนักงานบัญชี (Accountant)":
    st.subheader("💰 หน้าต่างบันทึกบัญชีและการเงิน (สิทธิ์: พนักงานบัญชี)")
    
    tab1, tab2 = st.tabs(["💵 ตารางตรวจสอบค่าน้ำมันและกำไร", "🗺️ แผนที่พิกัด"])
    
    with tab1:
        st.write("💡 พนักงานบัญชีสามารถพิมพ์แก้ไขช่อง 'ค่าน้ำมัน' และ 'กำไร' ได้โดยตรง:")
        edited_df = st.data_editor(df, use_container_width=True, hide_index=True)
        
        if st.button("💾 บันทึกตัวเลขบัญชี", use_container_width=True, type="primary"):
            st.session_state['trucks_db'] = edited_df
            st.success("✅ บันทึกยอดบัญชีการเงินเรียบร้อยแล้ว!")
            st.rerun()
            
    with tab2:
        st.map(df)

# 🚛 กรณีที่ 3: สิทธิ์พนักงานขับรถ (มีฟอร์มพิมพ์กรอกข้อมูลภาษาไทย และซ่อนเรื่องเงิน)
elif user_role == "พนักงานขับรถ (Driver)":
    st.subheader("📝 ฟอร์มรายงานตัวสำหรับพนักงานขับรถ (สิทธิ์: พนักงานขับรถ)")
    
    tab1, tab2 = st.tabs(["✍️ พิมพ์ส่งรายละเอียดงาน", "🗺️ แผนที่ตำแหน่งรถในทีม"])
    
    with tab1:
        st.info("พี่ๆ คนขับกรอกรายละเอียดงานของตนเองลงในช่องด้านล่าง แล้วกดส่งข้อมูลได้เลยครับ")
        
        with st.form("driver_input_form"):
            # ช่องกรอกรายละเอียดหัวข้อภาษาไทยตามสั่งครับ
            input_truck_id = st.text_input("🆔 ทะเบียนรถของคุณ (เช่น TRK-04 หรือ กข-555):")
            input_driver_name = st.text_input("👤 ชื่อ-นามสกุล ของคุณ:")
            input_cargo = st.text_input("📦 สิ่งที่บรรทุก / รายละเอียดสินค้า:")
            input_eta = st.text_input("⏱️ เวลาคาดว่าจะถึง (ETA):")
            input_status = st.selectbox("🚦 สถานะรถปัจจุบันของคุณ:", ["กำลังวิ่ง", "จอดพัก", "ส่งงานเสร็จแล้ว"])
            
            submit = st.form_submit_button("📤 ส่งข้อมูลรายงานเข้าสู่แอปบริษัท", use_container_width=True)
            
            if submit:
                if input_truck_id == "" or input_driver_name == "":
                    st.error("❌ โปรดระบุ ทะเบียนรถ และ ชื่อของคุณ ก่อนกดส่งครับ")
                else:
                    # ถ้าป้อนทะเบียนซ้ำให้บันทึกทับ ถ้าเป็นรถคันใหม่ให้สร้างแถวใหม่ในระบบ
                    if input_truck_id in df['ทะเบียนรถ'].values:
                        st.session_state['trucks_db'].loc[df['ทะเบียนรถ'] == input_truck_id, ['ชื่อคนขับ', 'สิ่งที่บรรทุก', 'เวลาคาดว่าจะถึง (ETA)', 'สถานะ']] = [input_driver_name, input_cargo, input_eta, input_status]
                    else:
                        new_row = pd.DataFrame([{
                            "ทะเบียนรถ": input_truck_id, "ชื่อคนขับ": input_driver_name, 
                            "สิ่งที่บรรทุก": input_cargo, "เวลาคาดว่าจะถึง (ETA)": input_eta, 
                            "สถานะ": input_status, "ค่าน้ำมัน (บาท)": 0, "กำไร (บาท)": 0,
                            "latitude": 13.7563, "longitude": 100.5018
                        }])
                        st.session_state['trucks_db'] = pd.concat([st.session_state['trucks_db'], new_row], ignore_index=True)
                    
                    st.success(f"🎉 ส่งข้อมูลรถ {input_truck_id} เข้าตารางหลักของเถ้าแก่เรียบร้อยแล้ว!")
                    st.rerun()
                    
    with tab2:
        st.map(df)
